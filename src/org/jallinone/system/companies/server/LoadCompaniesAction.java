public class LoadCompaniesAction implements Action {


  public LoadCompaniesAction() {
  }

  /**
   * @return request name
   */
  public final String getRequestName() {
    return "loadCompanies";
  }


  /**
   * Business logic to execute.
   */
  public final Response executeCommand(Object inputPar,UserSessionParameters userSessionPars,HttpServletRequest request, HttpServletResponse response,HttpSession userSession,ServletContext context) {
    String serverLanguageId = ((JAIOUserSessionParameters)userSessionPars).getServerLanguageId();

    Connection conn = null;
    Statement stmt = null;
    try {
      conn = ConnectionManager.getConnection(context);

      // fires the GenericEvent.CONNECTION_CREATED event...
      EventsManager.getInstance().processEvent(new GenericEvent(
        this,
        getRequestName(),
        GenericEvent.CONNECTION_CREATED,
        (JAIOUserSessionParameters)userSessionPars,
        request,
        response,
        userSession,
        context,
        conn,
        inputPar,
        null
      ));

      stmt = conn.createStatement();
      ResultSet rset = stmt.executeQuery(
          "select SYS01_COMPANIES.COMPANY_CODE,REG04_SUBJECTS.NAME_1,REG04_SUBJECTS.PROGRESSIVE from SYS01_COMPANIES,REG04_SUBJECTS where "+
          "SYS01_COMPANIES.COMPANY_CODE=REG04_SUBJECTS.COMPANY_CODE_SYS01 and "+
          "SYS01_COMPANIES.ENABLED='Y' and "+
          "REG04_SUBJECTS.SUBJECT_TYPE='M'"
      );
      CompanyVO vo = null;
      ArrayList list = new ArrayList();
      while(rset.next()) {
        vo = new CompanyVO();
        vo.setCompanyCodeSYS01(rset.getString(1));
        vo.setName_1REG04(rset.getString(2));
        vo.setProgressiveREG04(rset.getBigDecimal(3));
        list.add(vo);
      }

      rset.close();
      Response answer = new VOListResponse(list,false,list.size());

      // fires the GenericEvent.BEFORE_COMMIT event...
      EventsManager.getInstance().processEvent(new GenericEvent(
        this,
        getRequestName(),
        GenericEvent.BEFORE_COMMIT,
        (JAIOUserSessionParameters)userSessionPars,
        request,
        response,
        userSession,
        context,
        conn,
        inputPar,
        answer
      ));

      return answer;

    }
    catch (Throwable ex) {
      Logger.error(userSessionPars.getUsername(),this.getClass().getName(),"executeCommand","Error while fetching companies list",ex);
      return new ErrorResponse(ex.getMessage());
    }
    finally {
      try {
        stmt.close();
      }
      catch (Exception ex2) {
      }
      try {
        ConnectionManager.releaseConnection(conn, context);
      }
      catch (Exception ex1) {
      }
    }

  }



}
