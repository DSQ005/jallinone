public class DeleteCustomFunctionsAction implements Action {


  public DeleteCustomFunctionsAction() {
  }


  /**
   * @return request name
   */
  public final String getRequestName() {
    return "deleteCustomFunctions";
  }


  /**
   * Business logic to execute.
   */
  public final Response executeCommand(Object inputPar,UserSessionParameters userSessionPars,HttpServletRequest request, HttpServletResponse response,HttpSession userSession,ServletContext context) {
    Connection conn = null;
    Statement stmt = null;

    try {
      // delete the specified custom functions...
      conn = ConnectionManager.getConnection(context);

      // fires the GenericEvent.CONNECTION_CREATED event...
      EventsManager.getInstance().processEvent(new GenericEvent(
        this,
        getRequestName(),
        GenericEvent.CONNECTION_CREATED,
        (JAIOUserSessionParameters)userSessionPars,
        request,
        response,
        userSession,
        context,
        conn,
        inputPar,
        null
      ));

      stmt = conn.createStatement();
      ArrayList rows = (ArrayList)inputPar;
      String functionCode = null;
      for(int i=0;i<rows.size();i++) {
        functionCode = (String)rows.get(i);
        stmt.execute(
            "delete from SYS22_CUSTOM_COLUMNS where FUNCTION_CODE_SYS06='"+functionCode+"'"
        );
        stmt.execute(
            "delete from SYS16_CUSTOM_FUNCTIONS where FUNCTION_CODE_SYS06='"+functionCode+"'"
        );
        stmt.execute(
            "delete from SYS18_FUNCTION_LINKS where FUNCTION_CODE_SYS06='"+functionCode+"'"
        );
        stmt.execute(
            "delete from SYS07_ROLE_FUNCTIONS where FUNCTION_CODE_SYS06='"+functionCode+"'"
        );
        stmt.execute(
            "delete from SYS06_FUNCTIONS where FUNCTION_CODE='"+functionCode+"'"
        );
      }

      Response answer = new VOListResponse(rows,false,rows.size());

      // fires the GenericEvent.BEFORE_COMMIT event...
      EventsManager.getInstance().processEvent(new GenericEvent(
        this,
        getRequestName(),
        GenericEvent.BEFORE_COMMIT,
        (JAIOUserSessionParameters)userSessionPars,
        request,
        response,
        userSession,
        context,
        conn,
        inputPar,
        answer
      ));

      conn.commit();

      // fires the GenericEvent.AFTER_COMMIT event...
      EventsManager.getInstance().processEvent(new GenericEvent(
        this,
        getRequestName(),
        GenericEvent.AFTER_COMMIT,
        (JAIOUserSessionParameters)userSessionPars,
        request,
        response,
        userSession,
        context,
        conn,
        inputPar,
        answer
      ));

      return answer;
    }
    catch (Throwable ex) {
      Logger.error(userSessionPars.getUsername(),this.getClass().getName(),"executeCommand","Error while deleting custom functions",ex);
      try {
        conn.rollback();
      }
      catch (Exception ex3) {
      }
      return new ErrorResponse(ex.getMessage());
    }
    finally {
      try {
        stmt.close();
      }
      catch (Exception ex2) {
      }
      try {
        ConnectionManager.releaseConnection(conn, context);
      }
      catch (Exception ex1) {
      }
    }

  }



}
